/**
 * Regenerates `src/constants/pet-asset-slugs.ts` from filenames in `public/assets/pets/*.png`.
 * Run after adding or removing pet preview images.
 *
 * Usage: node scripts/sync-pet-assets.mjs
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const petsDir = path.join(root, "public", "assets", "pets");
const outFile = path.join(root, "src", "constants", "pet-asset-slugs.ts");

if (!fs.existsSync(petsDir)) {
  console.error("Missing directory:", petsDir);
  process.exit(1);
}

const slugs = fs
  .readdirSync(petsDir)
  .filter((f) => f.toLowerCase().endsWith(".png"))
  .map((f) => f.replace(/\.png$/i, ""))
  .sort((a, b) => a.localeCompare(b));

const banner = `// Generated by scripts/sync-pet-assets.mjs — do not edit by hand.\n`;

const body = `${banner}export const PET_ASSET_SLUGS = ${JSON.stringify(slugs, null, 2)} as const;\n`;

fs.writeFileSync(outFile, body, "utf8");
console.log(`Wrote ${slugs.length} slug(s) to ${path.relative(root, outFile)}`);
